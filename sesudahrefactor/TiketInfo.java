package modul2.tugas.sesudahrefactor;

public class TiketInfo {
    public String nama;
    public String asal;
    public String tujuan;
    public double hargaTiket;
    public double diskon;

    public TiketInfo(String nama, String asal, String tujuan, double hargaTiket, double diskon) {
        this.nama = nama;
        this.asal = asal;
        this.tujuan = tujuan;
        this.hargaTiket = hargaTiket;
        this.diskon = diskon;
    }
}
